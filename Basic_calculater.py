a=float(input("Enter the No. : "))
b=float(input("Enter the No. : "))
o=input("Enter operation like(+,-,*,/) : ")
if o=='+' :
    c=a+b
    print(c)
elif o=='-' :
    c=a-b
    print(c)
elif o=='*' :
    c=a*b
    print(c)
elif o=='/' :
    c=a/b
    print(c)
else:
    print("Envalid")
